class BookRecommenderApp:
    """
    Root Tkinter window with two tabs: Popular Books and Recommend Books.
    """

    # Colour palette
    C_BG      = '#0A0E1A'
    C_PANEL   = '#141824'
    C_INPUT   = '#1E2436'
    C_ACCENT  = '#25D366'
    C_ACCENT2 = '#128C7E'
    C_WHITE   = '#FFFFFF'
    C_GREY    = '#8B95A8'
    C_LIGHT   = '#DCE4F0'
    C_BORDER  = '#2A3048'

    def __init__(self, root, model):
        """
        root  → tk.Tk() main window
        model → ModelLoader instance (already loaded)
        """
        self.root  = root
        self.model = model

        # Build recommendation engine
        self.engine = RecommendationEngine(
            pt                = model.pt,
            books_df          = model.books,
            similarity_scores = model.similarity_scores
        )

        self.all_titles = self.engine.all_titles()  # sorted list of all book titles
        self.img_cache  = ImageCache()              # shared image cache

        self._setup_window()
        self._build_header()
        self._build_notebook()

    # ── Window configuration ──────────────────────────────────────────────────
    def _setup_window(self):
        """Set title, size (90% screen), centre position, dark background."""
        self.root.title('📚  Book Recommender System')

        sw = self.root.winfo_screenwidth()
        sh = self.root.winfo_screenheight()
        w, h = int(sw * 0.90), int(sh * 0.87)
        x, y = (sw - w) // 2, (sh - h) // 2

        self.root.geometry(f'{w}x{h}+{x}+{y}')
        self.root.configure(bg=self.C_BG)
        self.root.resizable(True, True)
        self.root.columnconfigure(0, weight=1)
        self.root.rowconfigure(1, weight=1)   # notebook row expands on resize

    # ── Header bar ────────────────────────────────────────────────────────────
    def _build_header(self):
        """Top bar with green icon, title, subtitle, and stats badge."""
        hdr = tk.Frame(self.root, bg=self.C_PANEL)
        hdr.grid(row=0, column=0, sticky='ew')
        hdr.columnconfigure(1, weight=1)

        # Green icon box
        icon_box = tk.Frame(hdr, bg=self.C_ACCENT, width=54, height=54)
        icon_box.grid(row=0, column=0, padx=(20, 12), pady=14)
        icon_box.grid_propagate(False)
        tk.Label(icon_box, text='📚', font=('Segoe UI Emoji', 22),
                 bg=self.C_ACCENT, fg=self.C_WHITE).place(relx=.5, rely=.5, anchor='center')

        # Title block
        tb = tk.Frame(hdr, bg=self.C_PANEL)
        tb.grid(row=0, column=1, sticky='w')
        tk.Label(tb, text='Book Recommender System',
                 font=('Segoe UI', 17, 'bold'),
                 bg=self.C_PANEL, fg=self.C_WHITE).pack(anchor='w')
        tk.Label(tb, text='Popularity-Based  ·  Collaborative Filtering  ·  Cosine Similarity',
                 font=('Segoe UI', 9),
                 bg=self.C_PANEL, fg=self.C_GREY).pack(anchor='w')

        # Stats badge
        badge = tk.Frame(hdr, bg=self.C_INPUT, padx=14, pady=8)
        badge.grid(row=0, column=2, padx=(0, 20), pady=14)
        tk.Label(badge,
                 text=f'{len(self.model.popular_df)} Popular Books  ·  '
                      f'{len(self.all_titles)} for CF Recommendations',
                 font=('Segoe UI', 9),
                 bg=self.C_INPUT, fg=self.C_ACCENT).pack()

        # Thin green accent line below header
        tk.Frame(self.root, bg=self.C_ACCENT, height=3).grid(
            row=0, column=0, sticky='sew')

    # ── Notebook (2 tabs) ─────────────────────────────────────────────────────
    def _build_notebook(self):
        """Create styled ttk.Notebook with Popular and Recommend tabs."""
        s = ttk.Style()
        s.theme_use('clam')
        s.configure('App.TNotebook', background=self.C_BG, borderwidth=0)
        s.configure('App.TNotebook.Tab', background=self.C_PANEL, foreground=self.C_GREY,
                    padding=[20, 10], font=('Segoe UI', 11))
        s.map('App.TNotebook.Tab',
              background=[('selected', self.C_INPUT), ('active', self.C_INPUT)],
              foreground=[('selected', self.C_ACCENT), ('active', self.C_WHITE)])

        self.nb = ttk.Notebook(self.root, style='App.TNotebook')
        self.nb.grid(row=1, column=0, sticky='nsew', padx=10, pady=(6, 10))

        # Tab 1
        tab_pop = tk.Frame(self.nb, bg=self.C_BG)
        tab_pop.rowconfigure(1, weight=1)
        tab_pop.columnconfigure(0, weight=1)
        self.nb.add(tab_pop, text='🏆  Top 50 Popular Books')

        # Tab 2
        tab_rec = tk.Frame(self.nb, bg=self.C_BG)
        tab_rec.rowconfigure(2, weight=1)
        tab_rec.columnconfigure(0, weight=1)
        self.nb.add(tab_rec, text='🔍  Recommend Books')

        self._fill_popular_tab(tab_pop)
        self._fill_recommend_tab(tab_rec)

    # ── Tab 1 : Popular Books ─────────────────────────────────────────────────
    def _fill_popular_tab(self, parent):
        """Sub-header + 5-column scrollable grid of popular books."""

        # Sub-header
        sub = tk.Frame(parent, bg=self.C_PANEL, pady=10)
        sub.grid(row=0, column=0, sticky='ew', pady=(0, 6))
        tk.Label(sub, text='🏆  Top 50 Most Popular Books',
                 font=('Segoe UI', 13, 'bold'),
                 bg=self.C_PANEL, fg=self.C_WHITE).pack(side='left', padx=20)
        tk.Label(sub, text='Ranked by number of ratings  ·  minimum 250 ratings required',
                 font=('Segoe UI', 9),
                 bg=self.C_PANEL, fg=self.C_GREY).pack(side='left', padx=10)

        # Grid
        grid = BookGrid(parent, self.img_cache, cols=5)
        grid.grid(row=1, column=0, sticky='nsew', padx=4, pady=4)

        # Convert popular_df rows to list of dicts
        books_list = [
            {
                'title'       : row.get('Book-Title',  'Unknown'),
                'author'      : row.get('Book-Author', 'Unknown'),
                'image'       : row.get('Image-URL-M', ''),
                'num_ratings' : row.get('num_ratings',  0),
                'avg_rating'  : row.get('avg_rating',   0.0),
            }
            for _, row in self.model.popular_df.iterrows()
        ]

        grid.populate(books_list, show_rating=True)

    # ── Tab 2 : Recommend Books ───────────────────────────────────────────────
    def _fill_recommend_tab(self, parent):
        """Search bar + autocomplete dropdown + results grid."""

        # Search bar row
        search_bar = tk.Frame(parent, bg=self.C_PANEL, pady=14)
        search_bar.grid(row=0, column=0, sticky='ew', pady=(0, 4))

        tk.Label(search_bar, text='🔍  Type a book title:',
                 font=('Segoe UI', 11, 'bold'),
                 bg=self.C_PANEL, fg=self.C_WHITE).pack(side='left', padx=20)

        # StringVar — trace calls _on_type every time the user types
        self.search_var = tk.StringVar()
        self.search_var.trace_add('write', self._on_type)

        self.entry = tk.Entry(
            search_bar, textvariable=self.search_var,
            font=('Segoe UI', 11), bg=self.C_INPUT, fg=self.C_WHITE,
            insertbackground=self.C_WHITE, relief='flat', bd=0, width=42
        )
        self.entry.pack(side='left', padx=10, ipady=9)
        self.entry.bind('<Return>', lambda _e: self._do_recommend())   # Enter key

        tk.Button(
            search_bar, text='Get Recommendations  →',
            command=self._do_recommend,
            font=('Segoe UI', 10, 'bold'),
            bg=self.C_ACCENT, fg=self.C_WHITE,
            activebackground=self.C_ACCENT2, activeforeground=self.C_WHITE,
            relief='flat', cursor='hand2', padx=16, pady=9, bd=0
        ).pack(side='left', padx=10)

        # Autocomplete dropdown (hidden; placed dynamically below entry)
        self.dropdown_frame = tk.Frame(parent, bg=self.C_BORDER, bd=1)
        self.dropdown_list  = tk.Listbox(
            self.dropdown_frame,
            font=('Segoe UI', 10), bg=self.C_INPUT, fg=self.C_LIGHT,
            selectbackground=self.C_ACCENT, selectforeground=self.C_BG,
            relief='flat', bd=0, height=7, activestyle='none'
        )
        self.dropdown_list.pack(fill='both', expand=True)
        self.dropdown_list.bind('<Double-Button-1>', self._pick_suggestion)
        self.dropdown_list.bind('<Return>',          self._pick_suggestion)

        # Status label
        self.status_var = tk.StringVar(
            value='Type a book title above and press Enter or click the button.')
        tk.Label(parent, textvariable=self.status_var, font=('Segoe UI', 9),
                 bg=self.C_BG, fg=self.C_GREY
                 ).grid(row=1, column=0, sticky='w', padx=16, pady=(0, 4))

        # Results grid (4 columns)
        self.rec_grid = BookGrid(parent, self.img_cache, cols=4)
        self.rec_grid.grid(row=2, column=0, sticky='nsew', padx=4, pady=4)

    # ── Autocomplete ──────────────────────────────────────────────────────────
    def _on_type(self, *_args):
        """Filter all_titles on every keystroke and show the dropdown."""
        query = self.search_var.get().strip().lower()

        if len(query) < 2:
            self.dropdown_frame.place_forget()
            return

        matches = [t for t in self.all_titles if query in t.lower()][:10]

        if not matches:
            self.dropdown_frame.place_forget()
            return

        self.dropdown_list.delete(0, 'end')
        for t in matches:
            self.dropdown_list.insert('end', t)

        # Position dropdown just below the entry box
        ex = self.entry.winfo_x()
        ey = self.entry.winfo_y() + self.entry.winfo_height() + 26
        self.dropdown_frame.place(x=ex, y=ey, width=self.entry.winfo_width() + 20)
        self.dropdown_frame.lift()

    def _pick_suggestion(self, _event):
        """Fill entry with selected suggestion and run recommendation."""
        sel = self.dropdown_list.curselection()
        if not sel:
            return
        self.search_var.set(self.dropdown_list.get(sel[0]))
        self.dropdown_frame.place_forget()
        self._do_recommend()

    # ── Run recommendation ────────────────────────────────────────────────────
    def _do_recommend(self):
        """Read entry → call engine → populate results grid."""
        self.dropdown_frame.place_forget()
        book_name = self.search_var.get().strip()

        if not book_name:
            messagebox.showinfo('No Input', 'Please type a book title first.')
            return

        if book_name not in self.all_titles:
            hints = [t for t in self.all_titles if book_name.lower() in t.lower()][:4]
            hint_text = ('\n\nDid you mean:\n' + '\n'.join(f'  • {h}' for h in hints)
                         if hints else '')
            messagebox.showerror(
                'Book Not Found',
                f'"{book_name}" is not in the model.\n'
                f'Only books rated by 50+ active users are included.{hint_text}')
            return

        self.status_var.set(f'⏳  Finding books similar to "{book_name}" …')
        self.root.update_idletasks()   # force UI to repaint before heavy work

        try:
            results = self.engine.recommend(book_name, top_n=4)
            if not results:
                self.status_var.set('⚠️  No similar books found.')
                return
            self.rec_grid.populate(results, show_rating=False)
            self.status_var.set(f'✅  Showing {len(results)} books similar to "{book_name}"')
        except Exception as exc:
            messagebox.showerror('Error', str(exc))
            self.status_var.set('❌  Error — see dialog.')


# =============================================================================
#  SECTION 7 — ENTRY POINT
# =============================================================================

def main():
    """
    Entry point:
      1. Locate the folder containing this script (where .pkl files should live)
      2. Load all 4 model pickle files
      3. Create the Tkinter root window
      4. Build and launch the app
    """

    # Folder = same directory as app.py
    folder = os.path.dirname(os.path.abspath(__file__))
    print(f'Looking for model files in: {folder}')

    # Load model — show helpful error if any .pkl is missing
    try:
        model = ModelLoader(folder).load_all()
        print(f'  popular_df        : {len(model.popular_df)} rows')
        print(f'  pt (pivot table)  : {model.pt.shape}')
        print(f'  similarity_scores : {model.similarity_scores.shape}')
        print('Model loaded. Launching window …\n')

    except FileNotFoundError as err:
        tmp = tk.Tk()
        tmp.withdraw()   # hide blank root window
        messagebox.showerror('Missing Model Files', str(err))
        tmp.destroy()
        return

    # Create and run the app
    root = tk.Tk()
    BookRecommenderApp(root, model)
    root.mainloop()   # blocks until user closes the window


if __name__ == '__main__':
    main()
