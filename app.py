class BookGrid(tk.Frame):
    """
    A vertically scrollable canvas that arranges BookCard widgets in a grid.
    """

    GRID_BG = '#0A0E1A'

    def __init__(self, parent, img_cache, cols=5):
        super().__init__(parent, bg=self.GRID_BG)
        self.img_cache = img_cache
        self.cols      = cols
        self._build()

    def _build(self):
        """Create Canvas + Scrollbar + inner Frame."""
        self.canvas = tk.Canvas(self, bg=self.GRID_BG,
                                highlightthickness=0, bd=0)
        self.canvas.pack(side='left', fill='both', expand=True)

        vbar = ttk.Scrollbar(self, orient='vertical', command=self.canvas.yview)
        vbar.pack(side='right', fill='y')
        self.canvas.configure(yscrollcommand=vbar.set)

        self.inner = tk.Frame(self.canvas, bg=self.GRID_BG)
        self._win  = self.canvas.create_window((0, 0), window=self.inner, anchor='nw')

        # When inner frame resizes: update scroll region
        self.inner.bind('<Configure>',  lambda _e: self.canvas.configure(
            scrollregion=self.canvas.bbox('all')))
        # When canvas resizes: stretch inner frame to match
        self.canvas.bind('<Configure>', lambda  e: self.canvas.itemconfig(
            self._win, width=e.width))
        # Mouse-wheel scrolling
        self.canvas.bind_all('<MouseWheel>',
            lambda e: self.canvas.yview_scroll(int(-1*(e.delta/120)), 'units'))

    def populate(self, book_list, show_rating=False):
        """Clear grid and fill with BookCards from book_list."""
        for w in self.inner.winfo_children():
            w.destroy()   # remove all existing cards

        for idx, book in enumerate(book_list):
            row = idx // self.cols    # which row
            col = idx %  self.cols    # which column

            BookCard(
                parent      = self.inner,
                book_data   = book,
                img_cache   = self.img_cache,
                show_rating = show_rating
            ).grid(row=row, column=col, padx=8, pady=8, sticky='n')

        self.canvas.yview_moveto(0)   # scroll to top after populating


