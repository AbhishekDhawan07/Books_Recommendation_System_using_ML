# =============================================================================
#  SECTION 1 — MODEL LOADER
#  Loads all 4 .pkl files from disk and exposes them as attributes
# =============================================================================

class ModelLoader:
    """
    Reads the 4 pickle files saved by the Jupyter notebook.

    Attributes after calling .load_all()
    popular_df        → pd.DataFrame  : top-50 books
    pt                → pd.DataFrame  : pivot table rows=books, cols=User-IDs
    books             → pd.DataFrame  : full books table
    similarity_scores → np.ndarray    : (706,706) cosine similarity matrix
    """

    def __init__(self, folder='.'):
        # folder: directory where the .pkl files live
        self.folder = folder

    def _load_one(self, filename):
        """Open one pickle file and return its Python object."""
        path = os.path.join(self.folder, filename)   # build full file path

        if not os.path.exists(path):
            raise FileNotFoundError(
                f"Cannot find '{filename}' in:\n{self.folder}\n\n"
                f"Please run the Jupyter notebook completely first.\n"
                f"It will create '{filename}' in the same folder as your CSVs."
            )

        with open(path, 'rb') as f:
            return pickle.load(f)   # 'rb' = read binary mode

    def load_all(self):
        """Load all 4 files and store as instance attributes. Returns self."""
        self.popular_df        = self._load_one('popular.pkl')
        self.pt                = self._load_one('pt.pkl')
        self.books             = self._load_one('books.pkl')
        self.similarity_scores = self._load_one('similarity_scores.pkl')
        return self


# =============================================================================
#  SECTION 2 — RECOMMENDATION ENGINE
#  Wraps the recommend() logic from the Jupyter notebook as a clean class
# =============================================================================

class RecommendationEngine:
    """
    Given a book title, returns N similar books using cosine similarity.
    Same logic as the notebook's recommend() function, wrapped as a class.
    """

    def __init__(self, pt, books_df, similarity_scores):
        self.pt                = pt
        self.books_df          = books_df
        self.similarity_scores = similarity_scores

    def recommend(self, book_name, top_n=4):
        """
        Return top_n books most similar to book_name.

        Returns list of dicts with keys: title, author, image
        """
        # Find the integer position of book_name in the pivot table's row index
        # np.where returns a tuple; [0][0] extracts the first matching index
        index = np.where(self.pt.index == book_name)[0][0]

        # Sort all books by similarity descending, skip index 0 (the book itself)
        similar_items = sorted(
            list(enumerate(self.similarity_scores[index])),
            key=lambda x: x[1],   # sort by similarity score
            reverse=True
        )[1 : top_n + 1]           # take next top_n after skipping itself

        results = []
        for item_idx, _score in similar_items:
            title   = self.pt.index[item_idx]
            matched = self.books_df[self.books_df['Book-Title'] == title]
            matched = matched.drop_duplicates('Book-Title')

            if matched.empty:
                continue

            results.append({
                'title'  : matched['Book-Title'].values[0],
                'author' : matched['Book-Author'].values[0],
                'image'  : matched['Image-URL-M'].values[0],
            })

        return results

    def all_titles(self):
        """Return a sorted list of every book title in the pivot table."""
        return sorted(list(self.pt.index))


# =============================================================================
#  SECTION 3 — IMAGE CACHE
#  Downloads book cover images in background threads and caches the results
# =============================================================================

class ImageCache:
    """
    Downloads Amazon book cover URLs asynchronously and caches PhotoImages.
    Uses daemon threads so downloads never block the UI.
    """

    COVER_W = 120   # thumbnail width  (pixels)
    COVER_H = 165   # thumbnail height (pixels)

    def __init__(self):
        self._cache = {}         # maps URL (str) → PhotoImage
        self._placeholder = None # grey placeholder shown while loading

    def placeholder(self):
        """Return (or create) a grey placeholder PhotoImage."""
        if self._placeholder is None and PIL_AVAILABLE:
            img = Image.new('RGB', (self.COVER_W, self.COVER_H), color='#1E2436')
            self._placeholder = ImageTk.PhotoImage(img)
        return self._placeholder

    def get(self, url, on_done):
        """
        Return cached image immediately, or start a background download.
        on_done: callable(photo) called when the image is ready.
        """
        if not PIL_AVAILABLE or not REQUESTS_AVAILABLE or not url:
            return

        # Already cached — call on_done immediately
        if url in self._cache:
            on_done(self._cache[url])
            return

        def _worker():
            """Background thread: download → resize → cache → callback."""
            try:
                resp  = requests.get(url, timeout=6)
                resp.raise_for_status()

                img   = Image.open(BytesIO(resp.content))
                img   = img.resize((self.COVER_W, self.COVER_H), Image.LANCZOS)
                photo = ImageTk.PhotoImage(img)

                self._cache[url] = photo
                on_done(photo)            # update the Label widget in the UI

            except Exception:
                pass   # silently ignore network / decode errors

        # daemon=True: thread auto-dies when the main window closes
        threading.Thread(target=_worker, daemon=True).start()


