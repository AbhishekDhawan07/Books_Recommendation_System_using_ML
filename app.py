class BookCard(tk.Frame):
    """
    A compact card widget showing:
      cover image
      Book Title
      Author Name
      star rating  (only in Popular tab)
    """

    CARD_BG    = '#141824'
    TITLE_FG   = '#FFFFFF'
    AUTHOR_FG  = '#8B95A8'
    RATING_FG  = '#25D366'
    BORDER_COL = '#2A3048'

    def __init__(self, parent, book_data, img_cache, show_rating=False):
        """
        parent      → parent tk widget
        book_data   → dict: title, author, image, [num_ratings, avg_rating]
        img_cache   → shared ImageCache instance
        show_rating → if True, show star rating line
        """
        super().__init__(
            parent,
            bg=self.CARD_BG,
            padx=8, pady=10,
            highlightthickness=1,
            highlightbackground=self.BORDER_COL
        )

        self.book_data   = book_data
        self.img_cache   = img_cache
        self.show_rating = show_rating
        self._photo_ref  = None   # keep reference or GC will delete the image!

        self._build_widgets()

    def _build_widgets(self):
        """Create all child widgets inside this card."""

        # Cover image label — starts with placeholder, updated when download completes
        self.img_label = tk.Label(
            self,
            image=self.img_cache.placeholder(),
            bg=self.CARD_BG
        )
        self.img_label.pack()

        # Title label — wraplength auto-wraps long titles
        tk.Label(
            self,
            text=str(self.book_data.get('title', '')),
            font=('Segoe UI', 8, 'bold'),
            bg=self.CARD_BG,
            fg=self.TITLE_FG,
            wraplength=115,
            justify='center'
        ).pack(pady=(5, 1))

        # Author label — truncate very long names
        tk.Label(
            self,
            text=str(self.book_data.get('author', ''))[:24],
            font=('Segoe UI', 7),
            bg=self.CARD_BG,
            fg=self.AUTHOR_FG
        ).pack()

        # Rating line — only shown in the Popular tab
        if self.show_rating:
            nr  = self.book_data.get('num_ratings', 0)
            avg = self.book_data.get('avg_rating',  0.0)
            tk.Label(
                self,
                text=f'⭐ {float(avg):.1f}  ({int(nr):,} ratings)',
                font=('Segoe UI', 7),
                bg=self.CARD_BG,
                fg=self.RATING_FG
            ).pack(pady=(3, 0))

        # Start background image download
        url = str(self.book_data.get('image', ''))
        if url.startswith('http'):
            self.img_cache.get(url, self._set_image)

    def _set_image(self, photo):
        """Called by ImageCache when the cover download is complete."""
        self._photo_ref = photo               # keep reference to prevent GC
        self.img_label.config(image=photo)    # update label in the UI


